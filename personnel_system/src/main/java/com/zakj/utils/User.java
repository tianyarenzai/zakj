package com.zakj.utils;


import java.io.Serializable;

public class User implements Serializable {
    private static final long serialVersionUID = 1L;
    /**用户ID**/
    private String userId;
    /**用户登录账号**/
    private String userLogin;
    /**用户登录密码**/
    private String userPwd;
    /**用户登录密码秘钥**/
    private String userKey;
    /**用户手机号*/
    private String UPhone;
    /** 组织机构ID**/
    private String orgId;
    /**用户姓名**/
    private String userName;
    /**用户所属企业**/
    private String tenantId;
    /**用户注册时间**/
    private String createTime;
    /**用户性别**/
    private String userSex;
    /**用户身份证号**/
    private String userIdcar;
    /**微信用户唯一标识**/
    private String openid;
    /**终端；1：pc；2：app；3：小程序;4：pad;5:教师客户端**/
    private String terminal;
    /**用户访问角色：【0】游客；【1】教职工；【2】家长；【3】即使家长也是教职工；【4】学生；*/
    private String userRole;
    private String loginType;



    public String getUserLogin() {
        return userLogin;
    }

    public void setUserLogin(String userLogin) {
        this.userLogin = userLogin;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserPwd() {
        return userPwd;
    }

    public void setUserPwd(String userPwd) {
        this.userPwd = userPwd;
    }

    public String getUserKey() {
        return userKey;
    }

    public void setUserKey(String userKey) {
        this.userKey = userKey;
    }


    public String getOrgId() {
        return orgId;
    }

    public void setOrgId(String orgId) {
        this.orgId = orgId;
    }

    public String getUPhone() {
        return UPhone;
    }

    public void setUPhone(String UPhone) {
        this.UPhone = UPhone;
    }


    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getTenantId() {
        return tenantId;
    }

    public void setTenantId(String tenantId) {
        this.tenantId = tenantId;
    }

    public String getTerminal() {
        return terminal;
    }

    public void setTerminal(String terminal) {
        this.terminal = terminal;
    }

    public String getUserRole() {
        return userRole;
    }

    public void setUserRole(String userRole) {
        this.userRole = userRole;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }

    public String getUserIdcar() {
        return userIdcar;
    }

    public void setUserIdcar(String userIdcar) {
        this.userIdcar = userIdcar;
    }

    public String getUserSex() {
        return userSex;
    }

    public void setUserSex(String userSex) {
        this.userSex = userSex;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getLoginType() {
        return loginType;
    }

    public void setLoginType(String loginType) {
        this.loginType = loginType;
    }
}